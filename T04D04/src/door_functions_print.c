#include <stdio.h>
#include <math.h>

#define PI 3.1415927

double Agn_W(double x);
double Q_Hyp(double x);
double L_Ber(double x);

int main() {
    int debug;
    scanf("%d", &debug);
    char FIELD[21][43] = {
        "------------------------------------------",
        "....................|.....................",
        "....................|.....................",
        "....................|.....................",
        "....................|.....................",
        "....................|.....................",
        "....................|.....................",
        "....................|.....................",
        "....................|.....................",
        "....................|.....................",
        "....................|.....................",
        "....................|.....................",
        "....................|.....................",
        "....................|.....................",
        "....................|.....................",
        "....................|.....................",
        "....................|.....................",
        "....................|.....................",
        "....................|.....................",
        "....................|....................."
    };
    if (debug == 1) {
        for (int i = 0; i < 42; i++) {
            double x = -PI + i * 2 * PI / 41;
            FIELD[(int)(Agn_W(x) * 20)][i] = '*';
        }
    }
    if (debug == 2) {
        for (int i = 0; i < 42; i++) {
            double x = -PI + i * 2 * PI / 41;
            if (i > 20 && i < 30)
                FIELD[(int)(L_Ber(x) * 20)][(i - 10) * 2] = '*';
        }
    }
    if (debug == 3) {
        for (int i = 0; i < 42; i++) {
            double x = -PI + i * 2 * PI / 41;
            if ((int)(Q_Hyp(x) * 10) < 21)
                FIELD[(int)(Q_Hyp(x) * 10)][i] = '*';
        }
    }
    for (int i = 21; i >= 0; i--) {
        printf("%s\n", FIELD[i]);
    }
    return 0;
}

double Agn_W(double x) {
    return 1 / (1 + x * x);
}

double Q_Hyp(double x) {
    return 1 / (x * x);
}

double L_Ber(double x) {
    return sqrt(sqrt(1 + 4 * x * x) - x * x - 1);
}
